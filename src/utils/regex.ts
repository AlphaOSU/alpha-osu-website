export default {

  /**
   * 用户名
   */
  username: /^[\w[\]\s-]{4,20}$/,

  /**
   * 密码
   */
  password: /^(?=.*[A-z])(?=.*[0-9])[a-zA-Z0-9~!@#$%^&*_+=-]{8,16}$/,

  /**
   * 手机号
   */
  mobilePhone: /^1(3\d|4[5-9]|5[0-35-9]|6[567]|7[0-8]|8\d|9[0-35-9])\d{8}$/,

  /**
   * 身份证号
   */
  idCard: /^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$/,

  /**
   * qq 号
   */
  qq: /^[1-9][0-9]{4,12}$/,

  /**
   * 邮箱
   */
  email: /^[a-zA-Z0-9_-]+@[a-zA-Z0-9_-]+(\.[a-zA-Z0-9_-]+)+$/,

  /**
   * 验证码
   */
  code: /^[A-z0-9]{6}$/,
};
